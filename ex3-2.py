# 字串 (String)
name = "20"

print(name)
print(type(name))      # <class 'str'>
# 整數 (Integer)
age = 25
name = 20
print(type(age))       # <class 'int'>
print(name)
print(type(name))      # <class 'int'>
# 浮點數 (Float)
height = 175.6

print(type(height))    # <class 'float'>
# 布林值 (Boolean)
is_student = True
print(type(is_student)) # <class 'bool'>
